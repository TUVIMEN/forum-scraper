class Session(requests.Session):
    def __init__(self,**kwargs):
        super().__init__()

        self.wait = 0
        self.timeout = 120
        self.proxies = {}
        self.logfile = sys.stdout

        if kwargs.get('verify'):
            self.verify = kwargs.get('verify')
        if kwargs.get('wait'):
            self.wait = kwargs.get('wait')
        if kwargs.get('proxies'):
            self.wait = kwargs.get('proxies')
        if kwargs.get('timeout'):
            self.timeout = kwargs.get('timeout')
        if kwargs.get('logfile'):
            self.logfile = kwargs.get('logfile')

        self.get_renew(False)

    @staticmethod
    def new_useragent():
        r = random.randint(0,9)
        if r == 0:
            agents = useragents['opera']
        elif r >= 1 and r <= 3:
            agents = useragents['mozilla']
        else:
            agents = useragents['google']

        return agents[random.randint(0,len(agents)-1)]

    def get_renew(self,clear=True):
        if clear:
            self.close()
        self.headers.update({'User-Agent':self.new_useragent()})

    def get_req(self,url,**kwargs):
        if self.wait != 0:
            time.sleep(self.wait)

        if self.logfile:
            print(url,file=self.logfile)
        resp = self.get(url,timeout=self.timeout,proxies=self.proxies,**kwargs)

        if resp.status_code != 200:
            return None

        return resp

    def get_html(self,url,trim=False,**kwargs):
        resp = self.get_req(url,**kwargs)
        if resp == None:
            return None

        r = resp.text
        if trim:
            r = r.translate(str.maketrans('','','\t\n\r\a'))

        return reliq(r)

    def get_json(self,url):
        resp = self.get_req(url)
        if resp == None:
            return None

        return resp.json()
